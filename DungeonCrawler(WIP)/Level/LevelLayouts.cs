    public static void ChangeLayoutToPassable(int level, int x, int z)
    {
        if (level == 1)
        {
            level1[-z, x] = 0;
        }
        else if (level == 2)
        {
            level2[-z, x] = 0;
        }
        else if (level == 3)
        {
            level3[-z, x] = 0;
        }
    }

    public static void ChangeLayoutToImpassable(int level, int x, int z, string objectName)
    {
        int index = 42;

        if (objectName == "Door")
        {
            index = 2;
        }
        else if (objectName == "SecretDoor")
        {
            index = 3;
        }
        else if (objectName == "Enemy")
        {
            index = 99;
        }

        if (level == 1)
        {
            level1[-z, x] = index;
        }
        else if (level == 2)
        {
            level2[-z, x] = index;
        }
        else if (level == 3)
        {
            level3[-z, x] = index;
        }
    }
